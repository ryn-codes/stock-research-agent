function computeConfidenceScore(
  financial: financialOutput,
  news: newsOutput,
  competitive: competitiveOutput,
  thesis: thesisOutput,
  evidenceCount: number,
): ConfidenceScore {
  const financialDataQuality = financial
    ? financial.hasDataGap ? 60 : 90
    : 20;

  const newsCoverage = news
    ? news.hasDataGap ? 40 : Math.min(90, 50 + news.articles.length * 5)
    : 20;

  const competitiveAnalysis = competitive
    ? competitive.hasDataGap ? 45 : Math.min(85, 50 + competitive.competitors.length * 7)
    : 20;

  const thesisCoherence = thesis ? 80 : 20;

  const citationDensity = Math.min(90, evidenceCount * 8);

  const overall = Math.round(
    financialDataQuality * 0.30 +
    newsCoverage * 0.20 +
    competitiveAnalysis * 0.20 +
    thesisCoherence * 0.20 +
    citationDensity * 0.10,
  );

  const breakdown = {
    financialDataQuality,
    newsCoverage,
    competitiveAnalysis,
    thesisCoherence,
    citationDensity,
  };

  const gaps = [];
  if (!financial || financial.hasDataGap) gaps.push('incomplete financial data');
  if (!news || news.hasDataGap) gaps.push('limited news coverage');
  if (!competitive || competitive.hasDataGap) gaps.push('limited competitive data');
  if (!thesis) gaps.push('thesis generation failed');

  const rationale = gaps.length > 0
    ? `Confidence reduced due to: ${gaps.join(', ')}. Available data is ${overall >= 70 ? 'sufficient' : 'limited'} for the analysis.`
    : `High confidence — complete data available across all dimensions with ${evidenceCount} evidence points.`;

  return { overall, breakdown, rationale };
}

function compileCitations(
  evidence: Evidence[],
  financial: financialOutput,
  news: newsOutput,
  company: any,
): Citation[] {
  const seen = new Set<string>();
  const citations: Citation[] = [];

  // 1. Add Screener.in as a primary financial source if available
  const cleanTicker = (company.ticker || company.nseCode || '').trim().toUpperCase().replace(/[^A-Z0-9_-]/g, '');
  if (cleanTicker) {
    const screenerUrl = `https://www.screener.in/company/${cleanTicker}/`;
    seen.add(screenerUrl);
    citations.push({
      displayId: '',
      title: `Screener.in — ${company.companyName} Financials & Ratios`,
      sourceName: 'Screener.in',
      url: screenerUrl,
      citationType: 'financial_data',
      accessedAt: new Date().toISOString(),
    });
  }

  const webCandidates: Citation[] = [];
  const indianApiCandidates: Citation[] = [];

  // Helper to categorize and push
  const addCandidate = (url: string, title: string, sourceName: string, type: 'news_article' | 'financial_data') => {
    const cleanedUrl = url.trim();
    if (!cleanedUrl || seen.has(cleanedUrl.toLowerCase())) return;
    seen.add(cleanedUrl.toLowerCase());

    const citation: Citation = {
      displayId: '',
      title,
      sourceName,
      url: cleanedUrl,
      citationType: type,
      accessedAt: new Date().toISOString(),
    };

    if (cleanedUrl.toLowerCase().includes('indianapi.in') || cleanedUrl.toLowerCase().includes('indianapi.co')) {
      indianApiCandidates.push(citation);
    } else {
      webCandidates.push(citation);
    }
  };

  // 2. Collect from news articles
  if (news) {
    news.articles.forEach((article: AnalyzedArticle) => {
      if (article.url) {
        addCandidate(
          article.url,
          article.title || 'News Article',
          article.source || 'News Source',
          'news_article',
        );
      }
    });
  }

  // 3. Collect from evidence pool
  evidence.forEach((e) => {
    if (e.sourceUrl) {
      addCandidate(
        e.sourceUrl,
        e.sourceName || e.evidenceType.replace('_', ' '),
        e.sourceName || e.sourceAgent,
        e.evidenceType.includes('news') ? 'news_article' : 'financial_data',
      );
    }
  });

  // 4. Add IndianAPI primary stock profile to IndianAPI candidates
  if (financial && !financial.hasDataGap) {
    const indianApiProfileUrl = `https://stock.indianapi.in/stock?name=${encodeURIComponent(company.companyName)}`;
    addCandidate(
      indianApiProfileUrl,
      `IndianAPI — ${company.companyName} Stock Profile`,
      'IndianAPI',
      'financial_data',
    );
  }

  // 5. Build final citation list ensuring >= 75% web citations (ratio: W / (W+I) >= 0.75 => I <= W / 3)
  const MAX_CITATIONS = 15;

  // Add web candidates up to MAX_CITATIONS - 1 (since Screener.in is already added as a web source)
  const webToTake = webCandidates.slice(0, MAX_CITATIONS - citations.length);
  citations.push(...webToTake);

  // Take IndianAPI candidates based on ratio
  const w = citations.length; // Number of web citations currently in list
  const maxI = Math.floor(w / 3);
  const indianApiToTake = indianApiCandidates.slice(0, Math.min(maxI, MAX_CITATIONS - citations.length));
  citations.push(...indianApiToTake);

  // Assign display IDs sequentially
  citations.forEach((c, index) => {
    c.displayId = `[${index + 1}]`;
  });

  return citations;
}

function buildConfidenceSectionMarkdown(
  score: ConfidenceScore,
  gaps: string[],
): string {
  const { overall, breakdown, rationale } = score;
  const scoreEmoji = overall >= 80 ? '🟢' : overall >= 60 ? '🟡' : '🔴';

  return `## Confidence Score & Data Quality

### Overall Confidence: ${scoreEmoji} ${overall}/100

${rationale}

### Score Breakdown

| Dimension | Score | Notes |
|-----------|-------|-------|
| Financial Data Quality | ${breakdown.financialDataQuality}/100 | IndianAPI real-time & financial statements |
| News Coverage | ${breakdown.newsCoverage}/100 | IndianAPI news + Tavily + Exa search |
| Competitive Analysis | ${breakdown.competitiveAnalysis}/100 | IndianAPI peer list & industry comparison |
| Thesis Coherence | ${breakdown.thesisCoherence}/100 | Evidence-backed synthesis |
| Citation Density | ${breakdown.citationDensity}/100 | Sources per claim |

${gaps.length > 0 ? `### ⚠️ Data Gaps\n\n${gaps.map((g) => `- ${g}`).join('\n')}` : '### ✅ No significant data gaps identified'}

### Disclaimer

*${FINANCIAL_DISCLAIMER}*`;
}

function buildCitationsSectionMarkdown(citations: Citation[]): string {
  if (citations.length === 0) {
    return '## Source Citations\n\nNo citations available.';
  }

  const citationLines = citations
    .map((c) => `${c.displayId} **${c.title}**  \n   *${c.sourceName}* — [${c.url}](${c.url})`)
    .join('\n\n');

  return `## Source Citations

${citationLines}

---
*All financial data sourced from IndianAPI. News data sourced from IndianAPI news feed, Tavily, and Exa. Report generated by ResearchGPT AI system.*`;
}

function alignCitations(content: string, citations: Citation[]): string {
  if (!content) return content;

  // Regex to match markdown links: [Text](URL)
  const markdownLinkRegex = /\[([^\]]+)\]\((https?:\/\/[^\s)]+)\)/g;

  return content.replace(markdownLinkRegex, (match, text, url) => {
    const cleanUrl = url.trim();

    // Find index of URL in citations (case-insensitive)
    let citationIndex = citations.findIndex(
      (c) => c.url.toLowerCase() === cleanUrl.toLowerCase()
    );

    if (citationIndex === -1) {
      // URL not in compileCitations list (perhaps added dynamically by an agent)
      // Add it so it displays in the final list
      let domain = 'Source';
      try {
        domain = new URL(cleanUrl).hostname.replace('www.', '');
      } catch {
        // Fallback
      }

      const newCitation: Citation = {
        displayId: `[${citations.length + 1}]`,
        title: text || `Source — ${domain}`,
        sourceName: domain,
        url: cleanUrl,
        citationType: 'news_article',
        accessedAt: new Date().toISOString(),
      };
      citations.push(newCitation);
      citationIndex = citations.length - 1;
    }

    const displayIndex = citationIndex + 1;

    // If the text is already a number/citation like "1" or "[1]", just output the citation link
    if (/^\[?\d+\]?$/.test(text.trim())) {
      return `[${displayIndex}](${cleanUrl})`;
    }

    // Otherwise, keep the text and append the superscript citation link
    return `${text} [${displayIndex}](${cleanUrl})`;
  });
}

type financialOutput = FinancialAgentOutput | null | undefined;
type newsOutput = NewsAgentOutput | null | undefined;
type competitiveOutput = CompetitiveAgentOutput | null | undefined;
type thesisOutput = ThesisAgentOutput | null | undefined;
